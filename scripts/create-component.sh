#!/bin/bash

# 🏗️ Component Scaffolding Script for Lokaa App
# Generates feature modules following feature-first architecture
# Usage: ./scripts/create-component.sh ComponentName feature-name [type]

set -e

# Configuration
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
SRC_DIR="$PROJECT_ROOT/src"

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
BLUE='\033[0;34m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

# Print colored output
print_info() { echo -e "${BLUE}ℹ️  $1${NC}"; }
print_success() { echo -e "${GREEN}✅ $1${NC}"; }
print_warning() { echo -e "${YELLOW}⚠️  $1${NC}"; }
print_error() { echo -e "${RED}❌ $1${NC}"; }

# Help function
show_help() {
    echo "🏗️  Lokaa Component Scaffolding Script"
    echo "=================================="
    echo ""
    echo "Usage: $0 <ComponentName> <feature-name> [type]"
    echo ""
    echo "Arguments:"
    echo "  ComponentName   - PascalCase component name (e.g., UserProfile)"
    echo "  feature-name    - kebab-case feature directory (e.g., user-management)"
    echo "  type           - Component type (optional)"
    echo ""
    echo "Component Types:"
    echo "  feature        - Full feature module with hooks, types, services (default)"
    echo "  component      - Simple component only"
    echo "  shared         - Shared component in src/shared/components"
    echo "  page           - Page component with routing"
    echo ""
    echo "Examples:"
    echo "  $0 UserProfile user-management"
    echo "  $0 Button ui shared"
    echo "  $0 NotificationCenter notifications feature"
    echo "  $0 SettingsPage settings page"
    echo ""
    echo "Generated Structure (feature type):"
    echo "  src/features/feature-name/"
    echo "  ├── components/"
    echo "  │   ├── ComponentName.tsx"
    echo "  │   └── index.ts"
    echo "  ├── hooks/"
    echo "  │   ├── useComponentName.ts"
    echo "  │   └── index.ts"
    echo "  ├── types/"
    echo "  │   ├── ComponentName.types.ts"
    echo "  │   └── index.ts"
    echo "  ├── services/"
    echo "  │   ├── ComponentNameService.ts"
    echo "  │   └── index.ts"
    echo "  ├── __tests__/"
    echo "  │   └── ComponentName.test.tsx"
    echo "  └── index.ts"
}

# Validate inputs
validate_inputs() {
    if [[ -z "$1" ]] || [[ -z "$2" ]]; then
        print_error "Missing required arguments"
        show_help
        exit 1
    fi

    # Validate component name (PascalCase)
    if [[ ! "$1" =~ ^[A-Z][a-zA-Z0-9]*$ ]]; then
        print_error "Component name must be PascalCase (e.g., UserProfile, NotificationCenter)"
        exit 1
    fi

    # Validate feature name (kebab-case)
    if [[ ! "$2" =~ ^[a-z][a-z0-9-]*$ ]]; then
        print_error "Feature name must be kebab-case (e.g., user-management, notification-center)"
        exit 1
    fi
}

