# Generate index file
generate_index() {
    local component_name="$1"
    local file_path="$2"
    local type="$3"
    
    case "$type" in
        "components")
            cat > "$file_path" << EOF
/**
 * ${component_name} Components Export
 * Generated by Lokaa Component Scaffolding Script
 */

export { ${component_name}, default } from './${component_name}';
export type { ${component_name}Props } from './${component_name}';
EOF
            ;;
        "hooks")
            cat > "$file_path" << EOF
/**
 * ${component_name} Hooks Export
 * Generated by Lokaa Component Scaffolding Script
 */

export { use${component_name}, default } from './use${component_name}';
EOF
            ;;
        "types")
            cat > "$file_path" << EOF
/**
 * ${component_name} Types Export
 * Generated by Lokaa Component Scaffolding Script
 */

export type {
  ${component_name}Props,
  ${component_name}Data,
  ${component_name}State,
  ${component_name}Actions,
} from './${component_name}.types';
EOF
            ;;
        "services")
            cat > "$file_path" << EOF
/**
 * ${component_name} Services Export
 * Generated by Lokaa Component Scaffolding Script
 */

export { ${component_name}Service, default } from './${component_name}Service';
EOF
            ;;
        "feature")
            cat > "$file_path" << EOF
/**
 * ${component_name} Feature Export
 * Generated by Lokaa Component Scaffolding Script
 */

// Components
export { ${component_name} } from './components';
export type { ${component_name}Props } from './components';

// Hooks
export { use${component_name} } from './hooks';

// Types
export type {
  ${component_name}Data,
  ${component_name}State,
  ${component_name}Actions,
} from './types';

// Services
export { ${component_name}Service } from './services';
EOF
            ;;
    esac
}

# Create directory structure
create_directories() {
    local base_path="$1"
    local component_type="$2"
    
    case "$component_type" in
        "feature")
            mkdir -p "$base_path/components"
            mkdir -p "$base_path/hooks"
            mkdir -p "$base_path/types"
            mkdir -p "$base_path/services"
            mkdir -p "$base_path/__tests__"
            ;;
        "shared")
            mkdir -p "$base_path"
            ;;
        "component"|"page")
            mkdir -p "$base_path"
            ;;
    esac
}

# Main scaffolding function
scaffold_component() {
    local component_name="$1"
    local feature_name="$2"
    local component_type="${3:-feature}"
    
    print_info "Creating ${component_type} component: ${component_name}"
    
    # Determine base path
    local base_path
    case "$component_type" in
        "shared")
            base_path="$SRC_DIR/shared/components/$feature_name"
            ;;
        "page")
            base_path="$SRC_DIR/views"
            ;;
        *)
            base_path="$SRC_DIR/features/$feature_name"
            ;;
    esac
    
    # Check if feature already exists
    if [[ -d "$base_path" ]] && [[ "$component_type" == "feature" ]]; then
        print_warning "Feature directory already exists: $base_path"
        echo -n "Continue and potentially overwrite files? (y/N): "
        read -r response
        if [[ ! "$response" =~ ^[Yy]$ ]]; then
            print_info "Cancelled"
            exit 0
        fi
    fi
    
    # Create directory structure
    create_directories "$base_path" "$component_type"
    
    # Generate files based on type
    case "$component_type" in
        "feature")
            # Types
            generate_component_types "$component_name" "$base_path/types/${component_name}.types.ts"
            generate_index "$component_name" "$base_path/types/index.ts" "types"
            
            # Component
            generate_component "$component_name" "$feature_name" "$base_path/components/${component_name}.tsx" "$component_type"
            generate_index "$component_name" "$base_path/components/index.ts" "components"
            
            # Hook
            generate_hook "$component_name" "$base_path/hooks/use${component_name}.ts"
            generate_index "$component_name" "$base_path/hooks/index.ts" "hooks"
            
            # Service
            generate_service "$component_name" "$base_path/services/${component_name}Service.ts"
            generate_index "$component_name" "$base_path/services/index.ts" "services"
            
            # Test
            generate_test "$component_name" "$feature_name" "$base_path/__tests__/${component_name}.test.tsx"
            
            # Main index
            generate_index "$component_name" "$base_path/index.ts" "feature"
            ;;
            
        "shared")
            generate_component "$component_name" "$feature_name" "$base_path/${component_name}.tsx" "$component_type"
            generate_test "$component_name" "$feature_name" "$base_path/${component_name}.test.tsx"
            ;;
            
        "component"|"page")
            generate_component "$component_name" "$feature_name" "$base_path/${component_name}.tsx" "$component_type"
            generate_test "$component_name" "$feature_name" "$base_path/${component_name}.test.tsx"
            ;;
    esac
    
    print_success "Successfully created ${component_type} component: ${component_name}"
    print_info "Location: $base_path"
    
    # Show next steps
    echo ""
    print_info "Next steps:"
    case "$component_type" in
        "feature")
            echo "  1. Update database table name in ${component_name}Service.ts"
            echo "  2. Define proper types in ${component_name}.types.ts"
            echo "  3. Implement business logic in use${component_name}.ts"
            echo "  4. Design the UI in ${component_name}.tsx"
            echo "  5. Add tests in __tests__/${component_name}.test.tsx"
            echo ""
            echo "Import in your app:"
            echo "  import { ${component_name} } from '@/features/${feature_name}';"
            ;;
        "shared")
            echo "  1. Implement the shared component logic"
            echo "  2. Add proper TypeScript types"
            echo "  3. Update shared components index file"
            echo ""
            echo "Import in your app:"
            echo "  import { ${component_name} } from '@/shared/components/${feature_name}';"
            ;;
        *)
            echo "  1. Implement the component logic"
            echo "  2. Add proper TypeScript types"
            echo "  3. Add to your routing or parent component"
            ;;
    esac
}

# Main execution
main() {
    # Check if help is requested
    if [[ "$1" == "-h" ]] || [[ "$1" == "--help" ]]; then
        show_help
        exit 0
    fi
    
    # Validate inputs
    validate_inputs "$@"
    
    local component_name="$1"
    local feature_name="$2"
    local component_type="${3:-feature}"
    
    # Validate component type
    case "$component_type" in
        "feature"|"component"|"shared"|"page")
            ;;
        *)
            print_error "Invalid component type: $component_type"
            echo "Valid types: feature, component, shared, page"
            exit 1
            ;;
    esac
    
    print_info "🏗️  Lokaa Component Scaffolding"
    print_info "Component: $component_name"
    print_info "Feature: $feature_name"
    print_info "Type: $component_type"
    echo ""
    
    # Create the component
    scaffold_component "$component_name" "$feature_name" "$component_type"
}

# Execute if script is run directly
if [[ "${BASH_SOURCE[0]}" == "${0}" ]]; then
    main "$@"
fi
