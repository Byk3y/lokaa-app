# Generate service
generate_service() {
    local component_name="$1"
    local file_path="$2"
    
    cat > "$file_path" << EOF
import { getSupabaseClient } from '@/integrations/supabase/client';
import type { ${component_name}Data } from '../types';

/**
 * ${component_name} Service
 * Handles data operations for ${component_name} component
 * Generated by Lokaa Component Scaffolding Script
 */
export class ${component_name}Service {
  private static supabase = getSupabaseClient();

  /**
   * Fetch ${component_name} data
   */
  static async getData(): Promise<${component_name}Data[]> {
    try {
      const { data, error } = await this.supabase
        .from('your_table_name') // Update table name
        .select('*');

      if (error) throw error;
      return data || [];
    } catch (error) {
      console.error('${component_name}Service.getData:', error);
      throw error;
    }
  }

  /**
   * Create new ${component_name} record
   */
  static async create(data: Partial<${component_name}Data>): Promise<${component_name}Data> {
    try {
      const { data: result, error } = await this.supabase
        .from('your_table_name') // Update table name
        .insert(data)
        .select()
        .single();

      if (error) throw error;
      return result;
    } catch (error) {
      console.error('${component_name}Service.create:', error);
      throw error;
    }
  }

  /**
   * Update ${component_name} record
   */
  static async update(id: string, data: Partial<${component_name}Data>): Promise<${component_name}Data> {
    try {
      const { data: result, error } = await this.supabase
        .from('your_table_name') // Update table name
        .update(data)
        .eq('id', id)
        .select()
        .single();

      if (error) throw error;
      return result;
    } catch (error) {
      console.error('${component_name}Service.update:', error);
      throw error;
    }
  }

  /**
   * Delete ${component_name} record
   */
  static async delete(id: string): Promise<void> {
    try {
      const { error } = await this.supabase
        .from('your_table_name') // Update table name
        .delete()
        .eq('id', id);

      if (error) throw error;
    } catch (error) {
      console.error('${component_name}Service.delete:', error);
      throw error;
    }
  }
}

export default ${component_name}Service;
EOF
}

