/**
 * Clear all storage items on sign out
 */
export const clearAuthStorage = (): void => {
  console.log('🧹 Immediately clearing client-side session state and storage...');
  
  // Clear localStorage items
  const localItemsToClear = [
    'selectedSpaceId', 
    'lastVisitedSpace',
    'lastCreatedSpace',
    'spaceData',
    'getSupabaseClient().auth.token', // Specific Supabase token key
    // Smart redirect cache keys - CRITICAL for preventing cross-user space access
    'lastActiveSpace',
    'ownedSpaces',
    'memberSpaces', 
    'lastRedirectTimestamp',
    'lastJoinedSpace'
  ];
  
  localItemsToClear.forEach(item => {
    try {
      localStorage.removeItem(item);
      console.log(`🧹 Cleared localStorage item: ${item}`);
    } catch (e) {
      console.warn(`Failed to remove ${item} from localStorage:`, e);
    }
  });
  
  // CRITICAL: Clear space ownership and membership cache items to prevent unauthorized access
  try {
    Object.keys(localStorage)
      .filter(key => key.startsWith('user_owns_space_') || key.startsWith('user_member_') || key.startsWith('setupGuideDismissed_'))
      .forEach(key => {
        console.log(`🧹 Clearing space cache item: ${key}`);
        localStorage.removeItem(key);
      });
  } catch (e) {
    console.warn('Failed to clear space cache items:', e);
  }
  
  // Clear sessionStorage items
  const sessionItemsToClear = [
    'selectedSpaceId', 
    'spaceData', 
    'redirect_after_login', 
    'coming_from_space_switcher',
    'userWantsDiscover',
    'active_tab_' // Prefix for active tab storage
  ];
  
  sessionItemsToClear.forEach(item => {
    try {
      // For prefix items, remove all matching items
      if (item.endsWith('_')) {
        Object.keys(sessionStorage)
          .filter(key => key.startsWith(item))
          .forEach(matchingKey => {
            console.log(`Removing session storage item with prefix ${item}:`, matchingKey);
            sessionStorage.removeItem(matchingKey);
          });
      } else {
        sessionStorage.removeItem(item);
        console.log(`🧹 Cleared sessionStorage item: ${item}`);
      }
    } catch (e) {
      console.warn(`Failed to remove ${item} from sessionStorage:`, e);
    }
  });
  
  // Clear any Supabase keys in localStorage (important for Safari and general cleanup)
  try {
    Object.keys(localStorage)
      .filter(key => key.startsWith('sb-')) // Catches all Supabase-related keys
      .forEach(key => {
        console.log(`Removing Supabase localStorage key: ${key}`);
        localStorage.removeItem(key);
      });
  } catch (e) {
    console.warn('Failed to clear Supabase local storage items:', e);
  }

  // Also try to clear any items in sessionStorage that might be prefixed with sb-
  try {
    Object.keys(sessionStorage)
      .filter(key => key.startsWith('sb-'))
      .forEach(key => {
        console.log(`Removing Supabase sessionStorage key: ${key}`);
        sessionStorage.removeItem(key);
      });
  } catch (e) {
    console.warn('Failed to clear Supabase session storage items:', e);
  }
  
  console.log('✅ Storage cleanup completed - all user-specific data cleared');
}; 