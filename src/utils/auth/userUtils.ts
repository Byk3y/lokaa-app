/**
 * Fetch user details from public.users table
 */
export const fetchUserDetails = async (userId: string): Promise<Database['public']['Tables']['users']['Row'] | null> => {
  try {
    console.log('AuthContext: Fetching user details for ID:', userId);
    const { data, error } = await getSupabaseClient()
      .from('users')
      .select('*')
      .eq('id', userId)
      .single();
      
    if (error) {
      console.error('AuthContext: Error fetching user details:', error);
      throw error;
    }
    
    if (data) {
      console.log('AuthContext: User details loaded:', {
        id: data.id,
        full_name: data.full_name,
        profile_url: data.profile_url
      });
      return data;
    } else {
      console.warn('AuthContext: No user details found for ID:', userId);
      return null;
    }
  } catch (error) {
    console.error('AuthContext: Error in fetchUserDetails:', error);
    return null;
  }
};

/**
 * Generate a unique user URL slug
 */
export async function generateUniqueUserSlug(user: User): Promise<string | null> {
  if (!user) return null;

  // Destructure needed properties to simplify type inference
  const userId = user.id;
  const userEmail = user.email;
  const meta = user.user_metadata;

  // Type assertions for clarity
  const username = meta.username as string | undefined;
  const firstName = meta.firstName as string | undefined;
  const lastName = meta.lastName as string | undefined;
  const fullName = meta.full_name as string | undefined;

  // Try username, email prefix, full name, or fallback to user id
  const potentialBases: (string | undefined | null)[] = [
    username,
    userEmail?.split('@')[0],
    (firstName && lastName) ? `${firstName}-${lastName}` : null,
    fullName,
    userId
  ];
  const baseCandidates: string[] = potentialBases.filter((s): s is string => typeof s === 'string' && s.length > 0);

  for (const base of baseCandidates) {
    let slug = generateSlug(base);
    let unique = false;
    let attempt = 0;
    let candidate = slug;
    while (!unique && attempt < 5) {
      try {
        // Check uniqueness in users table with a simpler approach
        const { data, error } = await getSupabaseClient()
          .from('users')
          .select('id')
          .eq('profile_url', candidate)
          .limit(1);

        if (error) {
          console.warn('Error checking URL uniqueness:', error);
          break;
        }

        if (!data || data.length === 0) {
          unique = true;
          slug = candidate;
          break;
        }
        
        // If not unique, append a short random string
        candidate = `${slug}-${Math.floor(1000 + Math.random() * 9000)}`;
        attempt++;
      } catch (err) {
        console.error('Exception checking URL uniqueness:', err);
        break;
      }
    }
    if (unique) return slug;
  }
  // Fallback: user id
  return user.id;
}

// FIXED: Add user URL cache to prevent repeated RPC calls
const userUrlCache = new Map<string, { url: string; timestamp: number }>();
const URL_CACHE_TTL = 10 * 60 * 1000; // 10 minutes

/**
 * Get cached user URL or fetch from database if needed
 */
export const getCachedUserUrl = async (userId: string): Promise<string | null> => {
  // Check cache first
  const cached = userUrlCache.get(userId);
  if (cached && Date.now() - cached.timestamp < URL_CACHE_TTL) {
    return cached.url;
  }

  try {
    // Fetch directly from users table instead of RPC
    const { data, error } = await getSupabaseClient()
      .from('users')
      .select('profile_url')
      .eq('id', userId)
      .single();
      
    if (error) {
      console.error('Error fetching user URL:', error);
      return null;
    }
    
    if (data?.profile_url) {
      // Cache the result
      userUrlCache.set(userId, {
        url: data.profile_url,
        timestamp: Date.now()
      });
      return data.profile_url;
    }
    
    return null;
  } catch (error) {
    console.error('Error in getCachedUserUrl:', error);
    return null;
  }
};

